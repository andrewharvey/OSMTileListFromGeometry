package metaTile;

public class RenderingTile {
	public int z;
	public int y;
	public int x;
	
	public RenderingTile(int z, int x, int y) {
		this.z = z;
		this.y = y;
		this.x = x;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + x;
		result = prime * result + y;
		result = prime * result + z;
		return result;
	}

	@Override
	public String toString() {
		return z + "/" + x + "/" + y;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		RenderingTile other = (RenderingTile) obj;
		if (x != other.x)
			return false;
		if (y != other.y)
			return false;
		if (z != other.z)
			return false;
		return true;
	}
}
